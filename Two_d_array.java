import java.util.*;
public class Two_d_array {
  public static void main(String[] args) {
    int[][] b={{2,5,12},{3,6,23},{7,9,56}};
     System.out.println("by using twod array the elements are");
     for (int i=0;i<=2;i++) {
        for (int j=0;j<=2;j++) {
       System.out.print(b[i][j]+" ");    
        }
    System.out.println();
  }
}
}