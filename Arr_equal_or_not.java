import java.util.*;
public class Arr_equal_or_not {
    public static void main(String[] args)
    {
        int[] r={3,5,7,9};
        int[] p={4,8,2,10};
        if (r==p) {
            System.out.println("two arrays are equal");
        }
        else {
            System.out.println("Two arrays are not equal");
        }
    }
}